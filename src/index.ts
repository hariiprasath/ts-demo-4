/**
 * Simple Hello World TypeScript program
 */

// Function to display a greeting message
function greet(name: string): string {
  return `Hello, ${name}! Welcome to TypeScript.`;
}

// Main function
function main(): void {
  const message = greet("World");
  console.log(message);
  
  // Additional information
  console.log("This is a simple TypeScript Hello World project.");
  console.log("You can build it using 'npm run build'");
  console.log("And run it using 'npm start'");
  
}

// Execute the main function
main(); 


